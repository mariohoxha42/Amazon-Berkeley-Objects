package org.mario.model.raw;

public class AboItemCoreMetadata extends AboCoreMetadata {
	private String item_dimensions;
	private String item_keywords;
	private String item_shape;
	private String item_weight;
	
	public String getItem_dimensions() {
		return item_dimensions;
	}
	
	public void setItem_dimensions(String item_dimensions) {
		this.item_dimensions = item_dimensions;
	}
	
	public String getItem_keywords() {
		return item_keywords;
	}
	
	public void setItem_keywords(String item_keywords) {
		this.item_keywords = item_keywords;
	}
	
	public String getItem_shape() {
		return item_shape;
	}
	
	public void setItem_shape(String item_shape) {
		this.item_shape = item_shape;
	}
	
	public String getItem_weight() {
		return item_weight;
	}
	
	public void setItem_weight(String item_weight) {
		this.item_weight = item_weight;
	}
}
